public class Datatpe {

    public static void main(String[] args) {
        System.out.println("integer ");
        byte variablebyte = 45;
        short variableshort = 56;
        int variableint = 455;
        long variablelong = 555;
        System.out.println(variablebyte + ", " + variableshort + ", " + variableint + ", " + variablelong);
        System.out.println("floating - point ");
        float variablefloat = 56.555f;
        double variabledouble = 4.55d;
        System.out.println(variablefloat + ", " + variabledouble);
        System.out.println("character");
        char variablechar = 's';
        System.out.println(variablechar);
        System.out.println("boolean");
        boolean variableboolean = true;
        System.out.println(variableboolean);


    }
}
