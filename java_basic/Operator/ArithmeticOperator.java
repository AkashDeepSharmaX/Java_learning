public class ArithmeticOperator {
    public static void main(String[] args) {
        System.out.println("-------------------------------------");
        System.out.println("Using arithmetic operators ");
        System.out.println("______________________");
        int a =8;
        int b= 3;
        System.out.println("addition of two integer values 'a' ,'b' using '+' to get the result ");
        int add = a+b;
        System.out.println("Addition of two operators : "+add);

        System.out.println("______________________");
        System.out.println("subtraction of two integer values 'a' ,'b' using '-' to get the result ");
        int sub = a-b;
        System.out.println("subtraction of two operators : "+sub);
        System.out.println("______________________");
        
        System.out.println("multipication of two integer values 'a' ,'b' using '*' to get the result ");
        int mult = a*b;
        System.out.println("multipication of two operators : "+mult);
        System.out.println("______________________");
        
        System.out.println("devision of two integer values 'a' ,'b' using '/' to get the result ");
        int dev = a/b;
        System.out.println("devision of two operators : "+dev);
        System.out.println("______________________");
        
        System.out.println("modulous of two integer values 'a' ,'b' using '%' to get the result ");
        int mod = a%b;
        System.out.println("modulous of two operators : "+mod);
        System.out.println("----------------------------------------");
    }
}
